public class TiposAtributos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        char ch;
        ch = 'X';//caracter aspas simples. valor de X=88
        System.out.println("O valor ch é : "+ch);
        ch++;//incremento. valor de Y=89
        System.out.println("O valor ch é : "+ch);
        ch=90;//numero da letra. valor de Z=90
        System.out.println("O valor ch é : "+ch);
    }
    
}
